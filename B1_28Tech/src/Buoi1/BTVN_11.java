package Buoi1;

import java.util.Scanner;

public class BTVN_11 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		System.out.println(n*2);
		System.out.println();
		System.out.println((long)n*10);
		System.out.println();
		System.out.println(n/2);
		System.out.println();
		System.out.printf("%.3f",(float)n/2);
	}
}
